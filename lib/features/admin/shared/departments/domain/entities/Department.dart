
class Department{
  final int departmentID;
  final int totalSemesters;
  final int totalStudents;
  final int totalTeachers;
  final int totalCourses;
  final int sectionLength;
  final String departmentName;
  final String departmentCode;
  final String headOfDepartment;
  final String contactPhone;

  Department({required this.departmentID, required this.totalSemesters, required this.totalStudents, required this.totalTeachers, required this.totalCourses, required this.departmentName, required this.departmentCode, required this.headOfDepartment, required this.contactPhone, required this.sectionLength});

}
